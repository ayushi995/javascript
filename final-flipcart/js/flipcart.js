function showlist(arr){
	
	document.getElementById("lists").innerHTML = "";
	
	var map = arr.map((item, index) => {
		var boxEl = document.createElement("div");
		boxEl.setAttribute("class", "box");
		
		var imageEl = document.createElement("div");
		imageEl.setAttribute("class", "image");

		var img = document.createElement("img");
		img.setAttribute("src", item.image);
		imageEl.appendChild(img);
		boxEl.appendChild(imageEl);
		
		var contentEl = document.createElement("div");
		contentEl.setAttribute("class", "content");
		var category = document.createElement("h1")
		category.innerHTML = item.categories;
		contentEl.appendChild(category);
		
		var color = document.createElement("h2")
		color.innerHTML = item.color;
		contentEl.appendChild(color);
		
		var fixPrice = document.createElement("span");
		fixPrice.setAttribute("class", "one");
		fixPrice.innerHTML = "Rs" + item.actual_price;
		contentEl.appendChild(fixPrice);
		
		var netPrice = document.createElement("span");
		netPrice.setAttribute("class", "two");
		netPrice.innerHTML = item.net_Price + "Rs";
		contentEl.appendChild(netPrice);
		
		
		
		var disPrice = document.createElement("h3");
		disPrice.innerHTML = item.discount + "% off";
		contentEl.appendChild(disPrice);

		boxEl.appendChild(contentEl);
		
		document.getElementById("lists").appendChild(boxEl);
	});
}

showlist(arr);


function search(searchkey){
	var filterarr = arr.filter((item, index) => item.categories.match(searchkey) || item.color.match(searchkey));
	showlist(filterarr);
}

